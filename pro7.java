class Wrapper
{
public static void main(String args[]){
byte b=10;
short s=20;
int i=50;
long l=40;
float f=10.0f;
double d=60.0d;
char c='a';
boolean b2=true;

Byte byo=b;
Short so=s;
Integer io=i;
Long lo=l;
Float fo=f;
Double dobj=d;
Character co=c;
Boolean bo= b2;
System.out.println(".........printing the object values.......... ");
System.out.println(" Byte object:"+byo);
System.out.println(" Short object:"+so);
System.out.println(" Integer object:"+io);
System.out.println(" Long object:"+lo);
System.out.println(" Float object:"+fo);
System.out.println(" Double object:"+dobj);
System.out.println(" Character object:"+co);
System.out.println(" Boolean object:"+bo);
byte byv=byo;
short sv=so;
int iv=io;
long lv=lo;
float fv=fo;
double dv=dobj;
char cv=co;
boolean bv=bo;
System.out.println(".........printing the primitive values.......... ");
System.out.println(" Byte value:"+byv);
System.out.println(" Short value:"+sv);
System.out.println(" Integer value:"+iv);
System.out.println(" Long value:"+lv);
System.out.println(" Float value:"+fv);
System.out.println(" Double value:"+dv);
System.out.println(" Character value:"+cv);
System.out.println(" Boolean value:"+bv);
}
}